    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required',
            'email' => 'required|email',
            'subject' => 'required',
            'message' => 'required'
        ]);

        // Store data in database
        $contact = Contact::create($request->all());

        if ($contact->id) {
            // Send email
            Mail::to(config('custom.contact'))->send(new ContactForm($contact));

            // return with success message
            return back()->with('success', 'Thanks for contacting us. We will get back to you within 48 hours.');
        }
        
        // return with error message
        return back()->withInput($request->input())->with('error', 'Something went wrong, please try again later.');
    }

